    {
        $validated = $request->validate([
            'content' => 'required|min:4',
            'dog_ear_id' => 'required',
        ]);
        $validated['user_id'] = Auth::user()->id;

        $request->user()->comments()->create($validated);

        return redirect( route('dogears.show', ['id'=>$request->dog_ear_id] ));
